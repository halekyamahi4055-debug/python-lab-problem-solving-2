numbers = [10, 5, 8, 20, 3]
print("Count of items:", len(numbers))    # Output: 5
print("Highest value:", max(numbers))     # Output: 20
print("Lowest value:", min(numbers))      # Output: 3
print("Total sum:", sum(numbers))         # Output: 46
print("Sorted list:", sorted(numbers))    # Output: [3, 5, 8, 10, 20]
